function setLang(lang) {

  $('.preloader').removeClass('preloader-deactivate');

  setTimeout(() => {
    localStorage.setItem("lang", lang);

    document.querySelectorAll("[data-i18n]").forEach(el => {
      const key = el.getAttribute("data-i18n");
      el.textContent = langData[lang][key] || "";
    });

    document.querySelectorAll(".current-lang").forEach(el => {
      el.textContent = lang.toUpperCase();
    });

    document.querySelectorAll(".current-lang-flag").forEach(img => {
      img.src = (lang === "eng") ? "img/en_US.png" : "img/th.png";
    });

    document.querySelectorAll(".lang-th").forEach(el => {
      el.style.display = (lang === "th") ? "none" : "block";
    });

    document.querySelectorAll(".lang-en").forEach(el => {
      el.style.display = (lang === "eng") ? "none" : "block";
    });

    $('.preloader').addClass('preloader-deactivate');

  }, 500);
}

document.addEventListener("DOMContentLoaded", () => {
  const lang = localStorage.getItem("lang") || "th";
  setLang(lang);
});
